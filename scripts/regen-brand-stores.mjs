#!/usr/bin/env node
/**
 * Regenerera BRAND_TO_STORES från feed-JSON i src/data/feed-matches/*.json.
 * Inkluderar BARA butiker som finns i src/data/affiliate-stores.ts (skapar inga nya).
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');

// 1. Load valid store names from affiliate-stores.ts (only these can appear in BRAND_TO_STORES)
const affSrc = fs.readFileSync(path.join(ROOT, 'src/data/affiliate-stores.ts'), 'utf8');
const validStores = new Set();
for (const m of affSrc.matchAll(/^\s*'([^']+)':\s*\{[\s\S]*?network:/gm)) validStores.add(m[1]);
console.error(`${validStores.size} stores in affiliate-stores.ts`);

// 2. Map feed filename to display store name (must match a key in affiliate-stores.ts)
const FILE_TO_STORE = {
  'jollyroom': 'Jollyroom',
  'baby_v': 'Baby V',
  'babyland': 'Babyland',
  'stor_liten': 'Stor och Liten',
  'polarn_o_pyret': 'Polarn O. Pyret',
  'leksaksaff_ren': 'Leksaksaffaren',
  'xplora': 'Xplora',
  'babysam': 'Babysam',
  'partykungen': 'Partykungen.se',
  // bookbeat borttagen 2026-05-14, ej relevant för bebis-niche (80 SEK fast per sälj, böcker)
};

// 3. Walk feed-matches and aggregate brand → Set<store>
const feedDir = path.join(ROOT, 'src/data/feed-matches');
const brandToStores = new Map();

for (const f of fs.readdirSync(feedDir)) {
  if (!f.endsWith('.json')) continue;
  const key = f.replace('.json', '');
  if (key === 'addrevenue') {
    // Addrevenue is multi-merchant — derive merchant from productUrl domain
    const data = JSON.parse(fs.readFileSync(path.join(feedDir, f), 'utf8'));
    for (const entry of Object.values(data)) {
      if (!entry.brand) continue;
      const brand = entry.brand.trim();
      if (!brand) continue;
      // Skip generic placeholder brand-names från feed-import
      if (['addrevenue', 'adtraction'].includes(brand.toLowerCase())) continue;
      let domain;
      try { domain = new URL(entry.productUrl).hostname.replace(/^www\./, ''); } catch { continue; }
      // Look up the merchant in valid stores by domain (some matched by baseUrl in affiliate-stores)
      let storeName = null;
      for (const candidate of validStores) {
        const baseHostMatch = affSrc.match(new RegExp(`'${candidate.replace(/[.+*?^$()|[\]{}]/g, '\\$&')}'[^}]*?baseUrl:\\s*'https?://(?:www\\.)?([^/']+)`));
        if (baseHostMatch && baseHostMatch[1] === domain) { storeName = candidate; break; }
      }
      if (!storeName) continue;
      const bLow = brand.toLowerCase();
      if (!brandToStores.has(bLow)) brandToStores.set(bLow, new Set());
      brandToStores.get(bLow).add(storeName);
    }
    continue;
  }
  const storeName = FILE_TO_STORE[key];
  if (!storeName) {
    console.error(`[SKIP] ${f} — no store mapping`);
    continue;
  }
  if (!validStores.has(storeName)) {
    console.error(`[SKIP] ${f} — store "${storeName}" not in affiliate-stores.ts`);
    continue;
  }
  const data = JSON.parse(fs.readFileSync(path.join(feedDir, f), 'utf8'));
  let added = 0;
  for (const entry of Object.values(data)) {
    if (!entry.brand) continue;
    const brand = entry.brand.trim();
    if (!brand) continue;
    const bLow = brand.toLowerCase();
    if (!brandToStores.has(bLow)) brandToStores.set(bLow, new Set());
    brandToStores.get(bLow).add(storeName);
    added++;
  }
  console.error(`  ${storeName}: ${added} entries with brand`);
}

console.error(`\nTotal unique brands: ${brandToStores.size}`);
const multi = [...brandToStores.values()].filter((s) => s.size >= 2).length;
console.error(`Brands with 2+ stores: ${multi}`);

// 4. Write brand-stores.ts (preserve format)
const sorted = [...brandToStores.entries()].sort((a, b) => b[1].size - a[1].size || a[0].localeCompare(b[0]));
const lines = sorted.map(([brand, stores]) => {
  const stArr = [...stores].sort();
  const escaped = brand.replace(/"/g, '\\"');
  return `  "${escaped}": [${stArr.map((s) => JSON.stringify(s)).join(',')}],`;
});

const content = `/**
 * Auto-generated brand -> stores mapping for bebisguiden.
 * Regenerated by scripts/regen-brand-stores.mjs from src/data/feed-matches/.
 * ${brandToStores.size} brands across ${validStores.size} stores.
 */

export const BRAND_TO_STORES: Record<string, string[]> = {
${lines.join('\n')}
};

/** Default-butiker att visa när inget märke detekteras (sorterat efter provision). */
export const DEFAULT_STORES = ['Jollyroom', 'Baby V', 'Babyland', 'Polarn O. Pyret', 'Stor och Liten'];

/** Hämta butikslista för ett märke (case-insensitive). */
export function getStoresForBrand(brand: string): string[] {
  if (!brand) return DEFAULT_STORES;
  return BRAND_TO_STORES[brand.toLowerCase()] || DEFAULT_STORES;
}
`;

fs.writeFileSync(path.join(ROOT, 'src/data/brand-stores.ts'), content);
console.error(`\nWrote ${lines.length} brand entries to src/data/brand-stores.ts`);
